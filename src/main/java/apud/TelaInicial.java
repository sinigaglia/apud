    private File showOpenDirectoryDialog() {
        Preferences pref = Preferences.userRoot().node(TelaInicial.class.getName());
        JFileChooser chooser = new JFileChooser();
        String path = pref.get("defaultDir", ".");
        chooser.setCurrentDirectory(new java.io.File(path));
        chooser.setDialogTitle("Choose folder");
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setAcceptAllFileFilterUsed(false);
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            pref.put("defaultDir", chooser.getSelectedFile().getParentFile().getAbsolutePath());
            return chooser.getSelectedFile();
        }
        return null;
    }

