    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
        ptm.limpa();
        t = new Thread(new Runnable() {
            @Override
            public void run() {
                stop.setEnabled(true);
                percentual.setEnabled(false);
                pesquisar.setEnabled(false);
                updateStatus("Searching for synonyms...");
                Sinonimos[] sinos = geraSinonimos();
                updateStatus("Synonyms vector ready. Searching...");
                parser.setPercentualMinimo(percentual.getValue());
                parser.setRegex(regex.isSelected());
                parser.realizaBusca(sinos);
                updateStatus("Done searching!");
                pesquisar.setEnabled(true);
                percentual.setEnabled(true);
                stop.setEnabled(false);
            }
        });
        t.start();
    }//GEN-LAST:event_pesquisarActionPerformed

    private void lerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lerActionPerformed
        TelaInicial referencia = this;
        t = new Thread(new Runnable() {                 //thread para varredura, processo concorrente ao
            @Override                                   //swing para n√£o travar interface durante leitura
            public void run() {
                ler.setEnabled(false);
                File p = new File(pasta.getText());
                if (p.isDirectory()) {
                    updateStatus("Parsing PDF files...");
                    parser = new PastaPDFParser(p, referencia, referencia, recursivo.isSelected());
                } else {
                    JOptionPane.showMessageDialog(referencia, "The selected folder is not valid.");
                }
                updateStatus("Parsing complete.");
                pesquisar.setEnabled(parser != null);
                ler.setEnabled(true);
            }
        });
        t.start();
    }//GEN-LAST:event_lerActionPerformed

