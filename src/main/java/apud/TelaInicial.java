    private Sinonimos[] geraSinonimos() {
        String pesquisa = (String) termos.getSelectedItem();
        String[] palavras = pesquisa.split("\\s");
        hlm.addHistorico(pesquisa);
        Sinonimos[] s = new Sinonimos[palavras.length];
        for (int i = 0; i < palavras.length; i++) {
            if (sinonimos.isSelected()) {
                palavras[i] = palavras[i].replace('_', ' ');
                updateStatus("Searching for synonyms: '" + palavras[i] + "'... ");
                s[i] = new Sinonimos(palavras[i]);
                updateStatus("Searching for synonyms: '" + palavras[i] + "'... done");
            } else {
                palavras[i] = palavras[i].replace('_', ' ');
                s[i] = new Sinonimos(palavras[i], false);
            }
        }
        return s;
    }

